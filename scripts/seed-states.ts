async function main() {
  await connectDB();
  let added = 0, skipped = 0;
  for (const [slug,name,nameHindi,capital,region,totalAssemblySeats,totalLokSabhaSeats,rulingParty,rulingPartySlug,cm,cmSlug] of STATES) {
    const exists = await State.findOne({ slug });
    if (exists) { skipped++; continue; }
    await State.create({ slug,name,nameHindi,capital,region,totalAssemblySeats,totalLokSabhaSeats,rulingParty,rulingPartySlug,cm,cmSlug });
    added++;
  }
  console.log(`✅ States: ${added} added, ${skipped} skipped`);
  process.exit(0);
}
main().catch(e => { console.error(e); process.exit(1); });
